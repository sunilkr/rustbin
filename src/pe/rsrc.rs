pub const HEADER_LENGTH: u64 = 16;

#[repr(u8)]
#[derive(Derivative)]
#[derivative(Debug, Default)]
pub enum ResourceType {
    #[derivative(Default)]
    UNKNOWN = 0,
    CURSOR = 1,
    BITMAP = 2,
    ICON = 3,
    MENU = 4,
    DIALOG = 5,
    STRING = 6,
    FONTDIR = 7,
    FONT = 8,
    ACCELERATOR = 9,
    RC_DATA = 10,
    MESSAGE_TABLE = 11,
    GROUP_CURSOR = 12,
    GROUP_ICON = 14,
    VERSION = 16,
    DLG_INCLUDE = 17,
    PLUG_PLAY = 19,
    VXD = 20,
    ANIMATED_CURSOR = 21,
    ANIMATED_ICON = 22,
    HTML = 23,
    MANIFEST = 24,
}

impl From<u32> for ResourceType {
    fn from(value: u32) -> Self {
        match value {
            01 => Self::CURSOR,
            02 => Self::BITMAP,
            03 => Self::ICON,
            04 => Self::MENU,
            05 => Self::DIALOG,
            06 => Self::STRING,
            07 => Self::FONTDIR,
            08 => Self::FONT,
            09 => Self::ACCELERATOR,
            10 => Self::RC_DATA,
            11 => Self::MESSAGE_TABLE,
            12 => Self::GROUP_CURSOR,
            14 => Self::GROUP_ICON,
            16 => Self::VERSION,
            17 => Self::DLG_INCLUDE,
            19 => Self::PLUG_PLAY,
            20 => Self::VXD,
            21 => Self::ANIMATED_CURSOR,
            22 => Self::ANIMATED_ICON,
            23 => Self::HTML,
            24 => Self::MANIFEST,
            _  => Self::UNKNOWN,
        }
    }
}

#[derive(Derivative)]
#[derivative(Debug, Default)]
pub struct ResourceString {
    pub length: HeaderField<u32>,
    pub value: HeaderField<String>,
}

impl Header for ResourceString {
    fn parse_bytes(bytes: &[u8], pos: u64) -> crate::Result<Self> where Self: Sized {
        let mut hdr = Self::default();
        let mut reader = ContentBase::new(bytes);
        let mut offset = pos;
        
        let mut cursor = Cursor::new(bytes);
        cursor.seek(SeekFrom::Start(offset))?;

        hdr.length = Self::new_header_field(cursor.read_u32::<LittleEndian>()?, &mut offset);
        hdr.value.value = reader.read_wchar_string_at_offset(pos)?;
        hdr.value.offset = offset;

        Ok(hdr)
    }

    fn is_valid(&self) -> bool {
        self.length.value > 0 && self.value.value.len() == self.length.value as usize
    }

    fn length() -> usize {
        todo!()
    }
}

#[derive(Derivative)]
#[derivative(Debug, Default)]
pub struct ResourceData {
    pub rva: HeaderField<u32>,
    pub size: HeaderField<u32>,
    pub code_page: HeaderField<u32>,
    reserved: HeaderField<u32>,
    pub value: HeaderField<Vec<u8>>,
}

impl Header for ResourceData {
    fn parse_bytes(bytes: &[u8], pos: u64) -> crate::Result<Self> where Self: Sized {
        let mut offset = pos;
        let mut hdr = Self::default();
        
        let mut cursor = Cursor::new(bytes);
        cursor.seek(SeekFrom::Start(offset))?;

        hdr.rva = Self::new_header_field(cursor.read_u32::<LittleEndian>()?, &mut offset);
        hdr.size = Self::new_header_field(cursor.read_u32::<LittleEndian>()?, &mut offset);
        hdr.code_page = Self::new_header_field(cursor.read_u32::<LittleEndian>()?, &mut offset);
        hdr.reserved = Self::new_header_field(cursor.read_u32::<LittleEndian>()?, &mut offset);

        Ok(hdr)
    }

    fn is_valid(&self) -> bool {
        self.reserved.value == 0
    }

    fn length() -> usize {
        todo!()
    }
}

#[derive(Derivative)]
#[derivative(Debug, Default)]
pub enum ResourceNode {
    Str(ResourceString),
    Data(ResourceData),
    #[derivative(Default)]
    Dir(ResourceDirectory,)
}

#[derive(Debug)]
pub enum DataType {
    STRING,
    DATA,
    DIR,
}

#[derive(Derivative)]
#[derivative(Debug, Default)]
pub struct ResourceEntry {
    pub is_string: bool,
    pub is_data: bool,
    pub id: u32,
    pub name_offset: HeaderField<u32>,
    pub data_offset: HeaderField<u32>,
    pub data: ResourceNode,
}

impl Header for ResourceEntry {
    fn parse_bytes(bytes: &[u8], pos: u64) -> crate::Result<Self> where Self: Sized {
        let mut hdr = Self::default();
        let mut offset = pos;

        let mut cursor = Cursor::new(bytes);
        cursor.seek(SeekFrom::Start(offset))?;

        hdr.name_offset = Self::new_header_field(cursor.read_u32::<LittleEndian>()?, &mut offset);
        hdr.data_offset = Self::new_header_field(cursor.read_u32::<LittleEndian>()?, &mut offset);

        if hdr.name_offset.value & 0x80000000 == 0 {
            hdr.is_string = false;
            hdr.id = hdr.name_offset.value & 0x7fffffff
        }
        else {
            hdr.is_string = true;
            hdr.id = 0;
        }

        hdr.is_data = hdr.data_offset.value & 0x80000000 == 0;
            
        Ok(hdr)
    }

    fn is_valid(&self) -> bool {
        todo!()
    }

    fn length() -> usize {
        8
    }
}

#[derive(Derivative)]
#[derivative(Debug, Default)]
pub struct ResourceDirectory {
    pub charactristics: HeaderField<u32>,
    pub timestamp: HeaderField<DateTime<Utc>>,
    pub major_version: HeaderField<u16>,
    pub minor_version: HeaderField<u16>,
    pub named_entry_count: HeaderField<u16>,
    pub id_entry_count: HeaderField<u16>,
    pub entries: Vec<ResourceEntry>,
}

impl Display for ResourceDirectory {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        write!(f, "{{ Charactristics: {:#08x}, Timestamp: {:?}, MajorVersion: {}, MinorVersion: {}, NumberOfNamedEntries: {}, NumberOfIdEntries: {} }}",
            self.charactristics.value, self.timestamp.value, self.major_version.value, self.minor_version, self.named_entry_count, self.id_entry_count
        )
    }
}

impl ResourceDirectory {
    pub fn parse_rsrc(&mut self, buf: &[u8]) -> crate::Result<()> {
        for i in 0..(self.named_entry_count.value + self.id_entry_count.value) {
            let pos = self.charactristics.offset + HEADER_LENGTH + (i*8) as u64;
            let entry = ResourceEntry::parse_bytes(&buf, pos)?;
            self.entries.push(entry);            
        }

        Ok(())
    }
}

impl Header for ResourceDirectory {
    fn parse_bytes(bytes: &[u8], pos: u64) -> crate::Result<Self> where Self: Sized {
        let bytes_len = bytes.len() as u64;
        let mut offset = pos;

        if bytes_len < HEADER_LENGTH {
            return Err ( 
                Box::new(Error::new (
                    ErrorKind::InvalidData, 
                    format!("Not enough data; Expected {}, Found {}", HEADER_LENGTH, bytes_len)
                ))
            );
        }

        let mut hdr = Self::default();
        let mut cursor = Cursor::new(bytes);
        cursor.seek(SeekFrom::Start(offset))?;

        hdr.charactristics = Self::new_header_field(cursor.read_u32::<LittleEndian>()?, &mut offset);
        
        let data = cursor.read_u32::<LittleEndian>()?;
        let nts = NaiveDateTime::from_timestamp_opt(data.into(), 0).ok_or(InvalidTimestamp{ data: data.into() })?;
        let ts = DateTime::<Utc>::from_utc(nts, Utc);
        hdr.timestamp = HeaderField {value: ts, offset:offset, rva: offset};
        offset += size_of::<u32>() as u64;

        hdr.major_version = Self::new_header_field(cursor.read_u16::<LittleEndian>()?, &mut offset);
        hdr.minor_version = Self::new_header_field(cursor.read_u16::<LittleEndian>()?, &mut offset);
        hdr.named_entry_count = Self::new_header_field(cursor.read_u16::<LittleEndian>()?, &mut offset);
        hdr.id_entry_count = Self::new_header_field(cursor.read_u16::<LittleEndian>()?, &mut offset);

        Ok(hdr)
    }

    fn is_valid(&self) -> bool {
        self.charactristics.value == 0
    }

    fn length() -> usize {
        todo!()
    }
}


